package com.AiaaS.Team4K.model;

public class PriceDTO {
	private double oilPrice;
    private double coalPrice;
    private double LNGPrice;
    private double uranPrice;
    
	public double getOilPrice() {
		return oilPrice;
	}
	public void setOilPrice(double oilPrice) {
		this.oilPrice = oilPrice;
	}
	public double getCoalPrice() {
		return coalPrice;
	}
	public void setCoalPrice(double coalPrice) {
		this.coalPrice = coalPrice;
	}
	public double getLNGPrice() {
		return LNGPrice;
	}
	public void setLNGPrice(double lNGPrice) {
		LNGPrice = lNGPrice;
	}
	public double getUranPrice() {
		return uranPrice;
	}
	public void setUranPrice(double uranPrice) {
		this.uranPrice = uranPrice;
	}
    
    
}
